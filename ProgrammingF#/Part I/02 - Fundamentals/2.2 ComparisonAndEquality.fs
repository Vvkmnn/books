// Intro

(* 
    You can compare numeric values using standard equality operators. All of which evaluate to a boolean value, except compare which returns -1, 0, or 1 depending less than, equal to, or greater. 
*)

// Less than 
1 < 2 // true

// Less than or Equal to
4 <= 4 // true

// Greater than
1 > 5 // false

// Greater than or Equal To
1 >= 5 // false

// Equal to
5 = 1 // false

// Not equal to
5 <> 1 // true

// compare
compare 31 24 // 1 for greater
compare 24 31 // -1 for less
compare 30 30 // 0 for same